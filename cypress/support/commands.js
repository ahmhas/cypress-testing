// -- Random email generator
Cypress.Commands.add('randomEmailGenerator', ()=> {
        function randomStringWithLength(length) {
            let result           = '';
            let characters       = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
            let charactersLength = characters.length;
            for ( let i = 0; i < length; i++ ) {
                result += characters.charAt(Math.floor(Math.random() * charactersLength));
            }
            return result;
        }
        // return 'testing' + randomStringWithLength(6) + '@tes.com'
    return cy.get('.mx-2 > input').focus().clear().type('testing' + randomStringWithLength(6) + '@tes.com')
})